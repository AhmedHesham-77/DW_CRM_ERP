	CST_ID INT,
	CST_KEY VARCHAR(10),
	CST_FIRSTNAME VARCHAR,
	CST_LASTNAME VARCHAR,
	CST_MARITAL_STATUS CHAR,
	CST_GNDR CHAR,
	CST_CREATE_DATE DATE
);

